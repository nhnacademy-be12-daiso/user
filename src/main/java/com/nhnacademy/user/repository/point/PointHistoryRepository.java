public interface PointHistoryRepository extends JpaRepository<PointHistory, Long> {

    // 특정 회원의 포인트 내역 페이징 조회
    Page<PointHistory> findAllByUserOrderByCreatedAtDesc(User user, Pageable pageable);

}
